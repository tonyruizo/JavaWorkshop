package exercise1_2;

// Define a public FiveMessages Class
public class FiveMessages {
	// Define a void main method
	public static void main(String[] args) {
		
		// Calling the displayMsg method five times
		displayMsg();
		displayMsg();
		displayMsg();
		displayMsg();
		displayMsg();
	}
	
	// Method to display/print out the message
	static void displayMsg() {
		
		System.out.print("Welcome to Java.\n");
	}
}
